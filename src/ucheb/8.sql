SELECT DISTINCT Н_КВАЛИФИКАЦИИ.НАИМЕНОВАНИЕ
FROM Н_УЧЕНИКИ
RIGHT JOIN Н_ЛЮДИ ON Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
JOIN Н_ПЛАНЫ ON Н_УЧЕНИКИ.ПЛАН_ИД = Н_ПЛАНЫ.ИД
JOIN Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ ON Н_ПЛАНЫ.НАПС_ИД = Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ.ИД
JOIN Н_КВАЛИФИКАЦИИ ON Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ.КВАЛ_ИД = Н_КВАЛИФИКАЦИИ.ИД
WHERE Н_ЛЮДИ.ФАМИЛИЯ = 'Афанасьев'
  AND Н_ЛЮДИ.ИМЯ = 'Дмитрий'
  AND Н_ЛЮДИ.ОТЧЕСТВО = 'Борисович';