SELECT ГРУППА 
FROM (
  SELECT Н_УЧЕНИКИ.ГРУППА AS ГРУППА, COUNT(*) AS КОЛВО
  FROM Н_УЧЕНИКИ
  JOIN Н_ПЛАНЫ ON Н_УЧЕНИКИ.ПЛАН_ИД = Н_ПЛАНЫ.ИД
  JOIN Н_ОТДЕЛЫ ON Н_ПЛАНЫ.ОТД_ИД = Н_ОТДЕЛЫ.ИД
  WHERE Н_ОТДЕЛЫ.КОРОТКОЕ_ИМЯ = 'КТиУ'
    AND Н_УЧЕНИКИ.НАЧАЛО < '2011-01-01'
    AND Н_УЧЕНИКИ.КОНЕЦ >= '2012-01-01'
  GROUP BY Н_УЧЕНИКИ.ГРУППА
) AS ГРУППА_КОЛВО
WHERE КОЛВО = 5;
